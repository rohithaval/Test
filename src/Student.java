
public class Student {
	int id;
	String name;
	int age;
	String address;

	void setId(int id) {
		this.id = id;
	}

	
	int getId() {
		return this.id;
	}

	void setName(String name) {
		this.name = name;
	}

	String getName() {
		return this.name;
	}

	void setAge(int age) {
		this.age = age;
	}

	int getAge() {
		return this.age;
	}

	void setAddress(String address) {
		this.address = address;
	}

	String getAddress() {
		return this.address;
	}

}
